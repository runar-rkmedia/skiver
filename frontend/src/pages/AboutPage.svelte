<h2>About</h2>

<p>
  <a href="https://github.com/runar-rkmedia/skiver/">Skiver</a> is an open-source
  translation-management-tool, currently available in early-access.
</p>

<p>Based on technologies:</p>

<ul>
  <li>
    Backend: Go
    <ul>
      <li><a href="https://goswagger.io/">Go-Swagger</a></li>
      <li><a href="https://github.com/schollz/mnemonicode">mnemonicode</a></li>
      <li><a href="https://masterminds.github.io/sprig/">sprig</a></li>
    </ul>
  </li>
  <li>Frontend: <a href="https://svelte.dev/">Svelte</a></li>
  <li>Bundler: <a href="https://esbuild.github.io/">esbuild</a></li>
  <li>Visual style: <a href="https://www.pollen.style/">Pollen.style</a></li>
  <li>Icons: <a href="https://fontawesome.com/">FontAwesome</a></li>
  <li>Internationalization: <a href="https://www.i18next.com/">i18next</a></li>
  <li>Fuzzy search: <a href="https://fusejs.io/">Fuse.js</a></li>
</ul>

